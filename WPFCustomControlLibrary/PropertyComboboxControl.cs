namespace WPFCustomControlLibrary
{
    /// <summary>
    /// Follow steps 1a or 1b and then 2 to use this custom control in a XAML file.
    ///
    /// Step 1a) Using this custom control in a XAML file that exists in the current project.
    /// Add this XmlNamespace attribute to the root element of the markup file where it is 
    /// to be used:
    ///
    ///     xmlns:MyNamespace="clr-namespace:WPFCustomControlLibrary"
    ///
    ///
    /// Step 1b) Using this custom control in a XAML file that exists in a different project.
    /// Add this XmlNamespace attribute to the root element of the markup file where it is 
    /// to be used:
    ///
    ///     xmlns:MyNamespace="clr-namespace:WPFCustomControlLibrary;assembly=WPFCustomControlLibrary"
    ///
    /// You will also need to add a project reference from the project where the XAML file lives
    /// to this project and Rebuild to avoid compilation errors:
    ///
    ///     Right click on the target project in the Solution Explorer and
    ///     "Add Reference"->"Projects"->[Browse to and select this project]
    ///
    ///
    /// Step 2)
    /// Go ahead and use your control in the XAML file.
    ///
    ///     <MyNamespace:PropertyComboboxControl/>
    ///
    /// </summary>
    public class PropertyComboBoxControl : Control
    {
        #region Dependency Properties


        #region Dependency Property - Title
        public string Title
        {
            get { return (string)GetValue(TitleProperty); }
            set { SetValue(TitleProperty, value); }
        }

        public static readonly DependencyProperty TitleProperty =
            DependencyProperty.Register(nameof(Title), typeof(string), typeof(PropertyComboBoxControl), new PropertyMetadata(default(string)));
        #endregion


        #region Dependency Property - SelectedItem
        public object SelectedItem
        {
            get { return (object)GetValue(SelectedItemProperty); }
            set { SetValue(SelectedItemProperty, value); }
        }

        public static readonly DependencyProperty SelectedItemProperty =
            DependencyProperty.Register(nameof(SelectedItem), typeof(object), typeof(PropertyComboBoxControl), new FrameworkPropertyMetadata(default(object)
                , FrameworkPropertyMetadataOptions.BindsTwoWayByDefault)
            { DefaultUpdateSourceTrigger = UpdateSourceTrigger.PropertyChanged });
        #endregion


        #region Dependency Property - ItemsSource
        public IEnumerable ItemsSource
        {
            get { return (IEnumerable)GetValue(ItemsSourceProperty); }
            set { SetValue(ItemsSourceProperty, value); }
        }

        public static readonly DependencyProperty ItemsSourceProperty =
            DependencyProperty.Register(nameof(ItemsSource), typeof(IEnumerable), typeof(PropertyComboBoxControl), new PropertyMetadata(default(IEnumerable)));
        #endregion

        #endregion

        #region Constructors
        static PropertyComboBoxControl()
        {
            DefaultStyleKeyProperty.OverrideMetadata(typeof(PropertyComboBoxControl), new FrameworkPropertyMetadata(typeof(PropertyComboBoxControl)));
        }
        #endregion
    }
}
